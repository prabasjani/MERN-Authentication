import mongoose from "mongoose";

const AuthSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      require: true,
    },
    email: {
      type: String,
      require: true,
    },
    password: {
      type: String,
      require: true,
    },
  },
  { timestamps: true }
);

export const AuthModel = mongoose.model("auth", AuthSchema);
